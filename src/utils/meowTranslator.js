// ── Main translator ───────────────────────────────────────────────────────────

export const translateToMeow = (text) => {
  if (!text.trim()) return '';

  const mood = detectMood(text);
  const moodSounds = S[mood] || S.neutral;

  const tokens = text.split(/(\s+)/);
  const result = [];
  let wordCount = 0;
  let nextInsert = 2 + Math.floor(Math.random() * 4);

  for (const token of tokens) {
    if (/^\s+$/.test(token)) { result.push(token); continue; }
    if (token.startsWith('@') || token.startsWith('#')) { result.push(token); continue; }

    // Preserve punctuation
    const punctMatch = token.match(/([!?.,~]+)$/);
    const punct = punctMatch?.[0] || '';
    const clean = punct ? token.slice(0, -punct.length) : token;
    const lower = clean.toLowerCase();

    // Exclamation → more intense meow
    const exclaim = punct.includes('!') ? '!' : '';

    const mapped = MAP[clean] || MAP[lower];
    if (mapped) {
      result.push(mapped + exclaim + (punct.replace(/!/g, '')));
    } else if (clean.length <= 1) {
      result.push(token); // keep single chars
    } else {
      const r = Math.random();
      if      (r < 0.18) result.push(rnd(moodSounds) + punct);
      else if (r < 0.30) result.push(rnd(S.neutral) + clean + punct);
      else if (r < 0.42) result.push(clean + '~เมี๊ยว' + punct);
      else if (r < 0.52) result.push(clean + 'เมียว' + punct);
      else if (r < 0.60) result.push('เมี๊ยว' + clean + punct);
      else               result.push(token);
    }

    wordCount++;
    if (wordCount >= nextInsert) {
      result.push(' ' + rnd(moodSounds));
      wordCount = 0;
      nextInsert = 2 + Math.floor(Math.random() * 4);
    }
  }

  // Ensure output ends with a meow sound
  const joined = result.join('').trimEnd();
  const endsOk = [...S.neutral, ...moodSounds].some(s => joined.endsWith(s.replace(/ /g, '')));
  const lastChars = joined.slice(-6);
  const hasEmoji = ['💕', '🐾', '😸', '😻', '🎾', '🐟', '📦', '😴', '😾', '😱'].some(e => lastChars.includes(e));
  return (endsOk || hasEmoji) ? joined : joined + ' ' + rnd(moodSounds) + '~';
};

